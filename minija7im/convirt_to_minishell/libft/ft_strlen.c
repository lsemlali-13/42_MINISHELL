#include "../includes/libft.h"

size_t	ft_strlen(const char *s)
{
	size_t	i;

	i = 0;
	while (s[i])
		i++;
	return (i);
}

size_t	ft_d_strlen(char **s)
{
	size_t	i;

	i = 0;
	while(s[i])
		i++;
	return (i);
}
